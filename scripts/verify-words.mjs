const FINALS = ['','ㄱ','ㄲ','ㄳ','ㄴ','ㄵ','ㄶ','ㄷ','ㄹ','ㄺ','ㄻ','ㄼ','ㄽ','ㄾ','ㄿ','ㅀ','ㅁ','ㅂ','ㅄ','ㅅ','ㅆ','ㅇ','ㅈ','ㅊ','ㅋ','ㅌ','ㅍ','ㅎ']
const COMPOUND_VOWEL = {'ㅐ':['ㅏ','ㅣ'],'ㅒ':['ㅑ','ㅣ'],'ㅔ':['ㅓ','ㅣ'],'ㅖ':['ㅕ','ㅣ'],'ㅘ':['ㅗ','ㅏ'],'ㅙ':['ㅗ','ㅏ','ㅣ'],'ㅚ':['ㅗ','ㅣ'],'ㅝ':['ㅜ','ㅓ'],'ㅞ':['ㅜ','ㅓ','ㅣ'],'ㅟ':['ㅜ','ㅣ'],'ㅢ':['ㅡ','ㅣ']}
const DOUBLE_CONSONANT = {'ㄲ':['ㄱ','ㄱ'],'ㄸ':['ㄷ','ㄷ'],'ㅃ':['ㅂ','ㅂ'],'ㅆ':['ㅅ','ㅅ'],'ㅉ':['ㅈ','ㅈ']}
const COMPOUND_FINAL = {'ㄳ':['ㄱ','ㅅ'],'ㄵ':['ㄴ','ㅈ'],'ㄶ':['ㄴ','ㅎ'],'ㄺ':['ㄹ','ㄱ'],'ㄻ':['ㄹ','ㅁ'],'ㄼ':['ㄹ','ㅂ'],'ㄽ':['ㄹ','ㅅ'],'ㄾ':['ㄹ','ㅌ'],'ㄿ':['ㄹ','ㅍ'],'ㅀ':['ㄹ','ㅎ'],'ㅄ':['ㅂ','ㅅ']}

function expandJamo(j) {
  if (DOUBLE_CONSONANT[j]) return DOUBLE_CONSONANT[j]
  if (COMPOUND_FINAL[j]) return COMPOUND_FINAL[j]
  if (COMPOUND_VOWEL[j]) return COMPOUND_VOWEL[j]
  return [j]
}

function countJamo(word) {
  let n = 0
  for (const char of word) {
    const code = char.charCodeAt(0)
    if (code < SYLLABLE_START || code > SYLLABLE_END) { n++; continue }
    const offset = code - SYLLABLE_START
    const finalIdx = offset % 28
    const vowelIdx = Math.floor(offset / 28) % 21
    const initialIdx = Math.floor(offset / 28 / 21)
    n += expandJamo(INITIALS[initialIdx]).length
    n += expandJamo(VOWELS[vowelIdx]).length
    if (FINALS[finalIdx]) n += expandJamo(FINALS[finalIdx]).length
  }
  return n
}

